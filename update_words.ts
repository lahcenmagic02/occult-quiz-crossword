const cleanWords = Array.from(new Set(
  rawWords.split('\n')
    .map(w => w.replace(/•/g, '').trim().toUpperCase().replace(/-/g, ''))
    .filter(w => w.length >= 3)
));

// The user strictly wants ONLY these words to be used as subwords.
const validWords = cleanWords;

function getLetterCounts(word: string) {
  const counts: Record<string, number> = {};
  for (const char of word) {
    counts[char] = (counts[char] || 0) + 1;
  }
  return counts;
}

function canForm(word: string, baseCounts: Record<string, number>) {
  const wordCounts = getLetterCounts(word);
  for (const char in wordCounts) {
    if (!baseCounts[char] || wordCounts[char] > baseCounts[char]) {
      return false;
    }
  }
  return true;
}

function getSubWords(base: string) {
  const baseCounts = getLetterCounts(base);
  const result = validWords.filter(word => {
    if (word.length > base.length) return false;
    return canForm(word, baseCounts);
  });
  return result.sort((a, b) => a.length - b.length || a.localeCompare(b));
}

const precomputed: Record<string, string[]> = {};
const validBases: string[] = [];

// Sort cleanWords by length to provide a progression
cleanWords.sort((a, b) => a.length - b.length || a.localeCompare(b));

for (const base of cleanWords) {
  const subWords = getSubWords(base);
  if (subWords.length >= 2) {
    precomputed[base] = subWords;
    validBases.push(base);
  }
}

// Select exactly 100 words for basicMagickWords, scaling exponentially in difficulty
const basicMagickWords: string[] = [];
const usedIndices = new Set<number>();

for (let i = 0; i < 100; i++) {
  // Exponential curve from 0 to validBases.length - 1
  let targetIndex = Math.floor(Math.pow(i / 99, 1.5) * (validBases.length - 1));
  
  // Find the nearest unused index
  let index = targetIndex;
  let offset = 0;
  while (usedIndices.has(index) && offset < validBases.length) {
    offset++;
    if (targetIndex + offset < validBases.length && !usedIndices.has(targetIndex + offset)) {
      index = targetIndex + offset;
    } else if (targetIndex - offset >= 0 && !usedIndices.has(targetIndex - offset)) {
      index = targetIndex - offset;
    }
  }
  
  usedIndices.add(index);
  basicMagickWords.push(validBases[index]);
}

// The rest go to advancedBasicMagickWords
const advancedBasicMagickWords = validBases.filter((_, i) => !usedIndices.has(i));

const content = 'export const basicMagickWords = ' + JSON.stringify(basicMagickWords) + ';\n' +
'export const advancedBasicMagickWords = ' + JSON.stringify(advancedBasicMagickWords) + ';\n' +
'export const sortedBases = ' + JSON.stringify(validBases) + ';\n' +
'export const precomputedWords: Record<string, string[]> = ' + JSON.stringify(precomputed, null, 2) + ';\n';

fs.writeFileSync('src/levelData.ts', content);
console.log("Done updating level data. Total words:", validBases.length);
